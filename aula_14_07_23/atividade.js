// Crie uma função que receba um vetor de números e retorne a soma de todos os elementos.
let vetor5 = [7, 10, 13]
function somar(vetor1){
  let soma = 0
  for(i = 0; i < vetor1.length; i++){
    soma += vetor1[i]
  }
  return soma;
}
console.log("a soma de todos os vetores é: " + somar(vetor5))
